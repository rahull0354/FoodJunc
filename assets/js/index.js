// cart logic

const cartIcon = document.querySelector(".cart-icon");
const cartTab = document.querySelector(".cart-tab");
const closeBtn = document.querySelector(".close-btn");
const cardList = document.querySelector(".card-list");
const cartList = document.querySelector(".cart-list");
const cartTotal = document.querySelector(".cart-total");
const cartValue = document.querySelector(".cart-value");
const hamburger = document.querySelector(".hamburger");
const mobileMenu = document.querySelector(".mobile-menu");
const bars = document.querySelector(".fa-bars");

cartIcon.addEventListener("click", () =>
  cartTab.classList.add("cart-tab-active")
);

closeBtn.addEventListener("click", () =>
  cartTab.classList.remove("cart-tab-active")
);

hamburger.addEventListener("click", () => {
  if (bars.classList.contains("fa-bars")) {
    mobileMenu.classList.toggle("mobile-menu-active");
    bars.classList.replace("fa-bars", "fa-xmark");
  } else {
    mobileMenu.classList.toggle("mobile-menu-active");
    bars.classList.replace("fa-xmark", "fa-bars");
  }
});

//loading the products.json file

const initApp = () => {
  fetch("./assets/json/products.json")
    .then((response) => response.json())
    .then((data) => {
      productList = data;
      showCards();
    });
};

let productList = [];
let cartProducts = [];

const showCards = () => {
  productList.forEach((product) => {
    const orderCard = document.createElement("div");
    orderCard.classList.add("order-card");

    orderCard.innerHTML = `
        <div class="card-image">
            <img src="${product.image}" alt="${product.name}">
        </div>
        <h4>${product.name}</h4>
        <h4 class="price">${product.price}</h4>
        <a href="#" class="btn card-btn">Add to Cart</a>
        `;

    cardList.appendChild(orderCard);

    const cardBtn = orderCard.querySelector(".card-btn");
    cardBtn.addEventListener("click", (e) => {
      e.preventDefault();
      addToCart(product);
    });
  });
};

//   updating the total price of the cart

const updateTotalPriceOfCart = () => {

  let totalPrice = 0;
  let totalQuantity = 0;

  document.querySelectorAll(".item").forEach((item) => {

    const quantity = parseInt(item.querySelector(".quantity-value").textContent);

    const price = parseFloat(
      item.querySelector(".item-total").textContent.replace("₹", "")
    );

    totalPrice += price;
    totalQuantity += quantity;
  });

  cartTotal.textContent = `₹${totalPrice.toFixed(2)}`;
  cartValue.textContent = totalQuantity;
};

const addToCart = (product) => {
  //checking if the product is already in the cart
  const existingProduct = cartProducts.find((item) => item.id === product.id);
  if (existingProduct) {
    alert("Product is already in the cart");
    return;
  }

  cartProducts.push(product);

  let quantity = 1;
  let price = parseFloat(product.price.replace("₹", ""));

  const cartItem = document.createElement("div");
  cartItem.classList.add("item");

  cartItem.innerHTML = `
    <div class="item-image">
        <img src="${product.image}" />
    </div>
    <div class="detail">
        <h4>${product.name}</h4>
        <h4 class="item-total">${product.price}</h4>
    </div>
    <div class="flex">
        <a href="#" class="quantity-btn minus">
            <i class="fa-solid fa-minus"></i>
        </a>
        <h4 class="quantity-value">${quantity}</h4>
        <a href="#" class="quantity-btn plus">
            <i class="fa-solid fa-plus"></i>
        </a>
    </div>
    `;

  cartList.appendChild(cartItem);
  updateTotalPriceOfCart();

  const plusBtn = cartItem.querySelector(".plus");
  const minusBtn = cartItem.querySelector(".minus");
  const quantityValue = cartItem.querySelector(".quantity-value");
  const itemTotal = cartItem.querySelector(".item-total");

  plusBtn.addEventListener("click", (e) => {
    e.preventDefault();
    quantity++;
    quantityValue.textContent = quantity;
    itemTotal.textContent = `₹${(price * quantity).toFixed(2)}`;
    updateTotalPriceOfCart();
  });

  minusBtn.addEventListener("click", (e) => {
    e.preventDefault();

    if (quantity > 1) {
      quantity--;
      quantityValue.textContent = quantity;
      itemTotal.textContent = `₹${(price * quantity).toFixed(2)}`;
      updateTotalPriceOfCart();
    } else {
      cartItem.classList.add("slide-out");

      setTimeout(() => {
        cartItem.remove();
        cartProducts = cartProducts.filter((item) => item.id !== product.id);
        updateTotalPriceOfCart();
      }, 300);
    }
  });
};

initApp();
